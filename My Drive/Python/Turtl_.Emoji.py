from turtle import *
#First color is pencolor second is fillcolor.
color('black', 'yellow')
continue_x = True
while continue_x:
    #head shape
    #------------
    color
    home()
    begin_fill()
    circle(140,360)
    end_fill()
    #mouth
    #------------
    
    








    #right eye
    #------------
    penup()
    goto(60,170)
    pendown()
    begin_fill()
    fillcolor('#3D2B1F')
    shape('circle')
    shapetransform(3.0,0.0,0.0,2.0)
    end_fill()
    stamp()
    #left Eye
    #------------
    penup()
    goto(-60,170)
    stamp()
    pendown()
    #left Eye
    #-------------
    
    

    continue_x = False
done()